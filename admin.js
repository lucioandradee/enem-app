    document.getElementById('gate').style.display           = 'none';
    document.getElementById('app').style.display            = 'flex';
    document.getElementById('topbar-email').textContent     = adminUser?.email ?? '-';

    await loadAll();
    startAutoRefresh();
    setupRealtime();
